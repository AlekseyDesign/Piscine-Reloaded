int		ft_putchar(char c);

void	ft_putstr(char *str)
{
	int t;

	t = 0;
	while (str[t])
	{
		ft_putchar(str[t++]);
	}
}

int		main(int argc, char *argv[])
{
	int i;

	i = 1;
	while (i < argc)
	{
		ft_putstr(argv[i]);
		ft_putchar('\n');
		i++;
	}
	return (0);
}
